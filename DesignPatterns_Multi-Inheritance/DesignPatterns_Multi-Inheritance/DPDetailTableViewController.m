#import "DPDetailTableViewController.h"
#import "BaseViewModel.h"
#import "DisplayDataProtocol.h"

@interface DPDetailTableViewController ()
@property (nonatomic, strong) BaseViewModel *viewModel;
@end

@implementation DPDetailTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
}

- (void)viewWillAppear:(BOOL)animated {
    [super viewWillAppear:animated];
    if (!self.viewModelIdentifier) {
        return;
    }
    NSString *plistPath = [[NSBundle mainBundle] pathForResource:@"ViewModelType" ofType:@"plist"];
    NSDictionary *dictionary = [NSDictionary dictionaryWithContentsOfFile:plistPath];
    self.viewModel = [NSClassFromString(dictionary[self.viewModelIdentifier]) new];
    [self.viewModel fetchDataWithCompleteHandler:^{
        [self.tableView reloadData];
    }];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
}

#pragma mark - Table view data source
- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.viewModel.dataArray.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    static NSString *identifier = @"detailCellIdentifier";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:identifier];
    if (!cell) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:identifier];
        id <DisplayDataProtocol>model = self.viewModel.dataArray[indexPath.row];
        cell.textLabel.text = model.title;
        cell.detailTextLabel.text = model.subTitle;
    }
    return cell;
}
